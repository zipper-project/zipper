package log

import (
	"io"
	"os"

	"github.com/Sirupsen/logrus"
	"github.com/zipper-project/zipper/common/utils"
)

var (
	defaultFormatter = &logrus.TextFormatter{
		TimestampFormat: "2006-01-02 15:04:05",
		FullTimestamp:   true,
	}
	defaultLevel  = "debug"
	defaultOutput = os.Stderr
)

func init() {
	SetFormatter(defaultFormatter)
	SetLevel(defaultLevel)
	SetOutput(defaultOutput)
}

// Logger wrappers access to logger instance
type Logger interface {
	Debug(args ...interface{})
	Print(args ...interface{})
	Info(args ...interface{})
	Warn(args ...interface{})
	Warning(args ...interface{})
	Error(args ...interface{})
	Panic(args ...interface{})
	Fatal(args ...interface{})

	Debugf(format string, args ...interface{})
	Printf(format string, args ...interface{})
	Infof(format string, args ...interface{})
	Warnf(format string, args ...interface{})
	Warningf(format string, args ...interface{})
	Errorf(format string, args ...interface{})
	Panicf(format string, args ...interface{})
	Fatalf(format string, args ...interface{})

	Debugln(args ...interface{})
	Println(args ...interface{})
	Infoln(args ...interface{})
	Warnln(args ...interface{})
	Warningln(args ...interface{})
	Errorln(args ...interface{})
	Panicln(args ...interface{})
	Fatalln(args ...interface{})
}

// New returns an standard logger or a file system logger according the filename
func New(args ...interface{}) (Logger, error) {
	if len(args) == 1 {
		if name, ok := args[0].(string); ok {
			f, err := utils.OpenFile(name)
			if err != nil {
				return nil, err
			}
			SetOutput(f)
		}

	}
	return logrus.StandardLogger(), nil
}

// SetFormatter sets the standard logger formatter.
func SetFormatter(formatter logrus.Formatter) {
	logrus.SetFormatter(formatter)
}

// SetLevel sets the standard logger level.
func SetLevel(lvl string) {
	level, err := logrus.ParseLevel(lvl)
	if err != nil {
		return
	}

	if level >= logrus.DebugLevel {
		AddHook(&CallerHook{})
	}

	logrus.SetLevel(level)
}

// SetOutput sets the standard logger output.
func SetOutput(out io.Writer) {
	logrus.SetOutput(out)
}

// GetLevel returns the standard logger level.
func GetLevel() logrus.Level {
	return logrus.GetLevel()
}

// AddHook adds a hook to the standard logger hooks.
func AddHook(hook logrus.Hook) {
	logrus.AddHook(hook)
}

